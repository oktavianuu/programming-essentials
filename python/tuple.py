# loop through tuple
tuple_1 = (1, 2, 3)
for elem in tuple_1:
    print(elem)

# check if element present or not
tuple_2 = (1, 2, 3, 4)
print(5 in tuple_2)
print(5 not in tuple_2)

# check the length of tuple
tuple_3 = (1, 2, 3, 5)
print(len(tuple_3))

# multiply and add
tuple_4 = tuple_1 + tuple_2
tuple_5 = tuple_3 * 2

print(tuple_4)
print(tuple_5)